    private void tabelaConsultaMonstroMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaConsultaMonstroMouseClicked
        // TODO add your handling code here:
        txtNomeMonstro.setText(tabelaConsultaMonstro.getValueAt(tabelaConsultaMonstro.getSelectedRow(), TabelaModeloConsultaMonstroBatalha.COLUNA_NOME_MONSTRO).toString());
        txtPontosDeVidaMonstro.setText(tabelaConsultaMonstro.getValueAt(tabelaConsultaMonstro.getSelectedRow(), TabelaModeloConsultaMonstroBatalha.COLUNA_PONTOS_VIDA_ATUAL).toString());
        btInserirMonstroEmBatalha.setEnabled(true);
    }//GEN-LAST:event_tabelaConsultaMonstroMouseClicked

