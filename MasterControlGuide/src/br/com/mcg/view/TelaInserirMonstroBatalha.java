    private void btInserirMonstroEmBatalhaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btInserirMonstroEmBatalhaActionPerformed
        // TODO add your handling code here:

        RepositorioMonstros rm = new RepositorioMonstros();
        rm.setNomeMonstro(txtNomeMonstro.getText());
        rm.setPontosDeVidaAtual(Integer.parseInt(txtPontosDeVidaMonstro.getText()));
        rm.setSituacao("Em Combate");
        RepositorioMonstroDao dao = new RepositorioMonstroDao();
        dao.cadastrarMonstroEmBatalha(rm);
        
        JOptionPane.showMessageDialog(null, "Monstro inserido em campo de batalha", "Inserção Monstro", JOptionPane.OK_OPTION,inserirMonstroBatalha);
        
        txtNomeMonstro.setText("");
        txtPontosDeVidaMonstro.setText("");
        btInserirMonstroEmBatalha.setEnabled(false);
    }//GEN-LAST:event_btInserirMonstroEmBatalhaActionPerformed

