    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        // TODO add your handling code here:
        int opcao = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja Excluir a Raça?", "Exclusão de Classe", JOptionPane.YES_NO_OPTION);
        if (opcao == 0){
            String raca = txtRaca.getText();
            dao.excluirRaca(raca);
            txtRaca.setText("");
        }
        btExcluir.setEnabled(false);
        tabelaRacaDePersonagem.setModel(new TabelaModeloRaca(new RacaDePersonagemDao().listarRaca()));
    }//GEN-LAST:event_btExcluirActionPerformed

 
    
