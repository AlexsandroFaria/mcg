    private void btCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadastrarActionPerformed
        // TODO add your handling code here:
        if (txtRaca.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Campo raça não pode ser nulo!");
        }else{
            RacaDePersonagem racaDePersonagem = new RacaDePersonagem();
            racaDePersonagem.setRaca(txtRaca.getText());
            
            dao = new RacaDePersonagemDao();
            dao.cadastrarRaca(racaDePersonagem);
            
            txtRaca.setText("");
        }
        tabelaRacaDePersonagem.setModel(new TabelaModeloRaca(new RacaDePersonagemDao().listarRaca()));
    }//GEN-LAST:event_btCadastrarActionPerformed

    private void tabelaRacaDePersonagemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaRacaDePersonagemMouseClicked
        // TODO add your handling code here:
        txtRaca.setText(tabelaRacaDePersonagem.getValueAt(tabelaRacaDePersonagem.getSelectedRow(), TabelaModeloRaca.COLUNA_RACA).toString());
        btExcluir.setEnabled(true);
    }//GEN-LAST:event_tabelaRacaDePersonagemMouseClicked

